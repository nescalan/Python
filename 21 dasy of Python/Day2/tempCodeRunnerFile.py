# Los operadores aritméticos son los que se utilizan para realizar operaciones matem
#áticas como suma, resta, multiplicación, división, etc.
# Los operadores aritméticos son:
# + (suma)
print(1 + 2) 

# - (resta)
print(5-2) 

# * (multiplicación)
print(4*5)

# / (división)
print(10/2)

# Módulo (devuelve el resto de una división)
print(10 % 3) # 1

# Potencia
print(2 ** 3) # 8
